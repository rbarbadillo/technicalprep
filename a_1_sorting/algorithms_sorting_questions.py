#1 - Sort 10 schools around your house by distance:
# Insertion sort - Small data

#2 - eBay sorts listings by the current Bid amount:
# Radix or counting sort - Numbers within a fixed range.

#3 - Sport scores on ESPN
# Quick sort

#4 - Massive database (can't fit all into memory) needs to sort through past year's user data
# Quicksort or heapsort. DON'T use mergesort (space complexity issues)

#5 - Almost sorted Udemy review data needs to update and add 2 new reviews
# Insertion sort

#6 - Temperature Records for the past 50 years in Canada
# Radix or counting sort if temperatures have no decimal places. Else, quicksort

#7 - Large user name database needs to be sorted. Data is very random.
# Do not use insert sort (random), probably also don't use quicksort if you can't pick an appropiate pivot (worst case time complexity O(n^^2))

#8 - You want to teach sorting for the first time
# Bubble sort